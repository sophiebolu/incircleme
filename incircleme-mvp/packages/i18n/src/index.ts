export type StringKey = keyof (typeof strings)['ca'];

// --- Active locale ---
// CA is the shipping default. `t()` / `formatPrice()` read `activeLocale`. It is set
// once at startup from the real device/region resolution (see apps/mobile/lib/
// deviceLocale → matchLocale), and may then be overridden by the DEV-gated review
// switcher (apps/mobile/lib/devLocale). When nothing matches, it stays `defaultLocale`.
let activeLocale: Locale = defaultLocale;
const localeListeners = new Set<() => void>();

export function setActiveLocale(locale: Locale): void {
  if (locale === activeLocale) return;
  activeLocale = locale;
  for (const fn of localeListeners) fn();
}
export function getActiveLocale(): Locale {
  return activeLocale;
}
export function subscribeLocale(fn: () => void): () => void {
  localeListeners.add(fn);
  return () => localeListeners.delete(fn);
}

const SUPPORTED: readonly Locale[] = ['ca', 'es', 'en'];
/**
 * Map a BCP-47 language tag (e.g. 'en-GB', 'es-419', 'ca-ES', 'EN') to a supported
 * app locale by its primary language subtag. Returns null when none match — callers
 * fall back to `defaultLocale` (ca). This is the real device/region resolution helper.
 */
export function matchLocale(tag: string | null | undefined): Locale | null {
  const lang = (tag ?? '').toLowerCase().split(/[-_]/)[0] ?? '';
  return (SUPPORTED as string[]).includes(lang) ? (lang as Locale) : null;
}

export function t(key: StringKey, locale: Locale = activeLocale): string {
  return strings[locale][key] ?? strings[defaultLocale][key];
}

/** Fills {placeholders} in a locked template. Unknown keys are left intact. */
export function interpolate(template: string, vars: Record<string, string>): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) =>
    key in vars ? vars[key]! : `{${key}}`,
  );
}

/**
 * Locale-aware price formatting (Addendum/§20 review note): ca/es "15,00 €",
 * en "€15.00". Always via Intl — never hand-rolled decimals.
 */
// BCP-47 locale for all Intl formatting (price + dates), keyed by app locale.
const INTL_LOCALE: Record<Locale, string> = { ca: 'ca-ES', es: 'es-ES', en: 'en-GB' };

export function formatPrice(
  cents: number,
  currency = 'EUR',
  locale: Locale = activeLocale,
): string {
  return new Intl.NumberFormat(INTL_LOCALE[locale], { style: 'currency', currency }).format(
    cents / 100,
  );
}

// Locale-aware date/time — month & weekday names follow the active locale (EN in
// English, ES in Spanish, CA default) instead of being pinned to Catalan.
export function formatDateTime(
  date: Date | string,
  opts: Intl.DateTimeFormatOptions,
  locale: Locale = activeLocale,
): string {
  return new Date(date).toLocaleString(INTL_LOCALE[locale], opts);
}
export function formatDate(
  date: Date | string,
  opts: Intl.DateTimeFormatOptions,
  locale: Locale = activeLocale,
): string {
  return new Date(date).toLocaleDateString(INTL_LOCALE[locale], opts);
}
export function formatTime(
  date: Date | string,
  opts: Intl.DateTimeFormatOptions,
  locale: Locale = activeLocale,
): string {
  return new Date(date).toLocaleTimeString(INTL_LOCALE[locale], opts);
}
